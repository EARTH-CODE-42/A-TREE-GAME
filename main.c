void draw_block(int i,int j,unsigned int c,int scale)
{
    int ii,jj;
    for (ii=i*scale;ii<(i+1)*scale;ii++)
        for (jj=j*scale;jj<(j+1)*scale;jj++)
            draw_pixel(ii,jj,c);
}

