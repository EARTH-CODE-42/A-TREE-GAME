void pixel_grass(pixel m[][WORLD_X],int x,int y)//关于草
{
    if (m[x-1][y].code!=0) pixel_delete(m,x,y);//草上面不允许有东西覆盖
    if (rand()%10) return;//1/10概率不管
    if (m[x][y].grow==1)
    {
        pixel_delete(m,x,y);
        pixel_add(m,x,y,8,0);
    }
}

void pixel_growgrass(pixel m[][WORLD_X],int x,int y)
{
    if (tree.wood<15) return;//分叉以后才能长草，否则浇不了水
    if (rand()%10) return;//1/10概率不管
    if (y==0||y==WORLD_X-1) return;//没人想数组越界
    if ((m[x-1][y].code==0)&&(m[x-1][y-1].code==0)&&(m[x-1][y+1].code==0))//草的密度不能太大
    {
        pixel_add(m,x-1,y,9,!(rand()%3));//1/3的草会变花
    }
}

void map_old(pixel m[][WORLD_X])//元素未更新
{
    int i,j;
    for (i=0;i<WORLD_Y;i++)
        for (j=0;j<WORLD_X;j++)
            m[i][j].is_new=0;
}

void map_clear(pixel m[][WORLD_X])//元素清屏
{
    int i,j;
    for (i=0;i<WORLD_Y;i++)
        for (j=0;j<WORLD_X;j++)
        {
            m[i][j].code=0;
            m[i][j].is_color=0;
        }
}

void pixel_upd(pixel m[][WORLD_X],int i,int j)
{
    switch (m[i][j].code)
    {
    case 1:
        pixel_drop(m,i,j,1);//斜45°掉落
        pixel_lr(m,i,j,0);
        break;
    case 2:
        pixel_drop(m,i,j,2);//斜45°掉落 遇水版本
        pixel_d2w(m,i,j);
        break;
    case 3://湿土
        pixel_growgrass(m,i,j);//湿土可能长花
        pixel_lr(m,i,j,1);//湿土版本左右摇摆
        pixel_drop(m,i,j,3);//经典版本 遇湿土版本
        break;
    case 4://种子
        pixel_drop(m,i,j,0);
        pixel_seed(m,i,j);
        break;
    case 5://木头
        pixel_wood(m,i,j);
        break;
    case 6://根
        pixel_root(m,i,j);
        break;
    case 7://树叶
        pixel_leaf(m,i,j);
        break;
    case 8://花
        pixel_flower(m,i,j);
        break;
    case 9://草地
        pixel_grass(m,i,j);
        break;
    default:
        break;
    }
}

