void generate_star()
{
    int i;
    for (i=0;i<MAXSTARS;i++)
    {
        stars[i].x = rand()%screen_w/zoom;
        stars[i].y = rand()%screen_h/zoom;
        stars[i].color = RGB(rand()%256,rand()%256,rand()%256);//随机色嘛
        stars[i].s = rand()%4;//越大表示星星移动越快
        stars[i].show = 0;//一开始不会一下子显示完星星，因为那样很跳脱
    }
}

void draw_star()
{
    int i;
    //先移动星星呵呵呵
    for (i=0;i<MAXSTARS;i++)
    {
        stars[i].x += (0.5+(float)stars[i].s)*0.1;
        //如果星星跑出去了就又回到左边
        if (stars[i].x>=screen_w/zoom)
        {
            stars[i].x = 0;
            stars[i].y = rand()%(screen_h/zoom);
            stars[i].show = 1;//从左边出来的才会显示
            if ((menu.time<400)||(menu.time>1000))//现在是白天
                stars[i].show = 0;
        }
        //画星星了
        if ((rand()%100)&&(stars[i].show==1))//星星有1/100概率眨眼
            draw_block((int)stars[i].y,(int)stars[i].x,stars[i].color,zoom);
    }
}

