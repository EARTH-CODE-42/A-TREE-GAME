// win32 event handler
static LRESULT win32_events(HWND, UINT, WPARAM, LPARAM);

static LRESULT win32_events(HWND hWnd, UINT msg,WPARAM wParam, LPARAM lParam)
{
	HDC hdc;
    switch (msg)
    {
    case WM_CLOSE:
        screen_exit = 1;
        break;
    case WM_LBUTTONDOWN:
		mouse_click=1;
		break;
	case WM_LBUTTONUP:
		mouse_click=0;
        mouse_have_click=0;
		break;
	case WM_MOUSEMOVE://鼠标移动事件
		hdc=GetDC(hWnd);//初始化HDC
		mouse_x=LOWORD(lParam);//鼠标的横坐标
		mouse_y=HIWORD(lParam);//鼠标的纵坐标
		ReleaseDC(hWnd,hdc);//搞完人走带门，释放HDC
		break;
    case WM_KEYDOWN:
        screen_keys[wParam & 511] = 1;
        break;
    case WM_KEYUP:
        screen_keys[wParam & 511] = 0;
        break;
    default:
        return DefWindowProc(hWnd, msg, wParam, lParam);
    }
    return 0;
}

void win32_dispatch(void)
{
    MSG msg;
    while (1)
    {
        if (!PeekMessage(&msg, NULL, 0, 0, PM_NOREMOVE))
            break;
        if (!GetMessage(&msg, NULL, 0, 0))
            break;
        DispatchMessage(&msg);
    }
}

void win32_update(void)
{
    HDC hDC = GetDC(screen_handle);
    //StretchBlt(hDC,0, 0, screen_w, screen_h, screen_dc, 0, 0, screen_w, screen_h, SRCCOPY);
    BitBlt(hDC, 0, 0, screen_w, screen_h, screen_dc, 0, 0, SRCCOPY);
    ReleaseDC(screen_handle, hDC);
    win32_dispatch();
}

// 初始化窗口并设置标题
int win32_init(int w, int h, char *title)
{
    WNDCLASSA wc = { CS_BYTEALIGNCLIENT, (WNDPROC)win32_events, 0, 0, 0,
                     NULL, NULL, NULL, NULL, "GAME"
                   };
    BITMAPINFO bi = { {
            sizeof(BITMAPINFOHEADER), w, -h, 1, 32, BI_RGB,
            w * h * 4, 0, 0, 0, 0
        }
    };
    RECT rect = { 0, 0, w, h };
    int wx, wy, sx, sy;
    LPVOID ptr;
    HDC hDC;

    win32_close();

    wc.hbrBackground = (HBRUSH)GetStockObject(BLACK_BRUSH);
    wc.hInstance = GetModuleHandle(NULL);
    wc.hCursor = LoadCursor(NULL, IDC_ARROW);
    if (!RegisterClass(&wc)) return -1;

    screen_handle = CreateWindow("GAME", title,
                                  WS_OVERLAPPEDWINDOW | WS_CAPTION | WS_SYSMENU | WS_MINIMIZEBOX,
                                  0, 0, 0, 0, NULL, NULL, wc.hInstance, NULL);
    if (screen_handle == NULL) return -2;

    screen_exit = 0;
    hDC = GetDC(screen_handle);
    screen_dc = CreateCompatibleDC(hDC);
    ReleaseDC(screen_handle, hDC);

    screen_hb = CreateDIBSection(screen_dc, &bi, DIB_RGB_COLORS, &ptr, 0, 0);
    if (screen_hb == NULL) return -3;

    screen_ob = (HBITMAP)SelectObject(screen_dc, screen_hb);
    screen_fb = (unsigned int*)ptr;
    screen_w = w;
    screen_h = h;
    screen_pitch = w * 4;

    AdjustWindowRect(&rect, GetWindowLong(screen_handle, GWL_STYLE), 0);
    wx = rect.right - rect.left;
    wy = rect.bottom - rect.top;
    sx = (GetSystemMetrics(SM_CXSCREEN) - wx) / 2;
    sy = (GetSystemMetrics(SM_CYSCREEN) - wy) / 2;
    if (sy < 0) sy = 0;
    SetWindowPos(screen_handle, NULL, sx, sy, wx, wy, (SWP_NOCOPYBITS | SWP_NOZORDER | SWP_SHOWWINDOW));
    SetForegroundWindow(screen_handle);

    ShowWindow(screen_handle, SW_NORMAL);
    win32_dispatch();

    memset(screen_keys, 0, sizeof(int) * 512);
    memset(screen_fb, 0, screen_w * screen_h * 4);
    return 0;
}

void win32_close(void)
{
    if (screen_dc)
    {
        if (screen_ob)
        {
            SelectObject(screen_dc, screen_ob);
            screen_ob = NULL;
        }
        DeleteDC(screen_dc);
        screen_dc = NULL;
    }
    if (screen_hb)
    {
        DeleteObject(screen_hb);
        screen_hb = NULL;
    }
    if (screen_handle)
    {
        CloseWindow(screen_handle);
        screen_handle = NULL;
    }
}
